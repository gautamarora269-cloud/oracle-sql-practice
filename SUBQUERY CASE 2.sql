USE SCOTT;

# WAQTD DNAME OF THE EMPLOYEES WHOS NAME IS SMITH
SELECT DNAME 
FROM DEPT 
WHERE DEPTNO = (SELECT DEPTNO FROM EMP WHERE ENAME = 'SMITH');


#.WAQTD LOC OF THE EMP WHOS EMPLOYEE NUMBER IS 7902
SELECT LOC
FROM DEPT
WHERE DEPTNO = (
    SELECT DEPTNO
    FROM EMP
    WHERE EMPNO = 7902
);

# WAQTD DNAME AND LOC ALONG WITH DEPTNO OF THE EMPLOYEE WHO’S NAME ENDS WITH ‘R’.
SELECT DEPTNO, DNAME, LOC
FROM DEPT
WHERE DEPTNO IN (
    SELECT DEPTNO
    FROM EMP
    WHERE ENAME LIKE '%R'
);

# WAQTD DNAME OF THE EMPLOYEE WHOS DESIGNATION IS PRESIDENT
SELECT DNAME 
FROM DEPT
WHERE DEPTNO = (SELECT DEPTNO FROM EMP WHERE JOB = 'PRESIDENT');

# WAQTD ENAME AND SALARIES OF THE EMPLOYEES WHO ARE WORKING IN THE LOCATION ‘CHICAGO’
SELECT ENAME, SAL
FROM EMP
WHERE DEPTNO IN (
    SELECT DEPTNO
    FROM DEPT
    WHERE LOC = 'CHICAGO'
);

#.WAQTD DETAILS OF THE EMPLOYEES WORKING IN SALES
SELECT *
FROM EMP 
WHERE DEPTNO  IN (SELECT DEPTNO FROM DEPT WHERE DNAME = 'SALES');

# WAQTD NAMES OF EMPLOYEES WORKING IN OPERATIONS DEPARTMENT
SELECT ENAME 
FROM EMP
WHERE DEPTNO IN (SELECT DEPTNO FROM DEPT WHERE DNAME = 'OPERATIONS');




































